def fix_mojibake(text):
    # This is a common pattern for CP1251 -> UTF-8 mojibake
    # Р Р°Р±РѕС‡РёР№ СЃС‚РѕР» -> Рабочий стол
    try:
        # Step 1: represent string as bytes using latin-1 (which preserves the raw bytes of the mojibake)
        # Step 2: represent those bytes as UTF-8
        # Step 3: decode as CP1251
        raw = text.encode('latin-1')
        return raw.decode('utf-8').encode('latin-1').decode('cp1251')
    except:
        return text

